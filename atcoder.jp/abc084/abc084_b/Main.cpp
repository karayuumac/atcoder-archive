template<class T>
void load(vector<T> &v, int n) {
  for (int i = 0; i < n; ++i) {
    T t;
    cin >> t;
    v.push_back(t);
  }
}

template<class T>
T max(vector<T> &v) {
  return *max_element(v.begin(), v.end());
}

template<class T>
T min(vector<T> &v) {
  return *min_element(v.begin(), v.end());
}

void py(bool isUpper = false) {
  if (isUpper) {
    p("YES");
  } else {
    p("Yes");
  }
}

void pn(bool isUpper = false) {
  if (isUpper) {
    p("NO");
  } else {
    p("No");
  }
}

int main() {
  int a, b; string s;
  cin >> a >> b >> s;

  bool c = false;
  for (int i = 0; i < a + b + 1; ++i) {
    if (i == a && s[i] == '-') {
      c = true;
      continue;
    } else if ('0' <= s[i] && s[i] <= '9') {
      continue;
    } else {
      pn();
      return 0;
    }
  }

  if (!c) {
    pn();
  } else {
    py();
  }
}